// src/app.js

// For loop
for (let i = 0; i < 5; i++) {
    console.log('For loop:', i);
}

// While loop
let j = 0;
while (j < 5) {
    console.log('While loop:', j);
    j++;
}

// Array iteration with forEach
let numbers = [1, 2, 3, 4, 5];
numbers.forEach(number => {
    console.log('forEach:', number);
});
